                            Wanneer je bijvoorbeeld last hebt van je knie is het weliswaar niet zo dat de
                            last blijft bestaan wanneer je deze niet op “denk”-niveau aanpakt, maar het
                            inzetten van je denken heeft wel invloed. Je denken maakt onderdeel uit van je
