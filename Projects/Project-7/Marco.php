                            <h2>Marco van Hooff</h2>
                            <p><strong class="subtitel">Reïntegratie psycholoog, psycholoog MDP, GZ-psycholoog, Directeur / Eigenaar</strong></p>

                            <p>Gewoon 'Marco', de meest voorkomende benaming bij de persoon die ik ben:
                            eigenaar, directeur, collega, teamplayer, knokker, psycholoog, denker &amp;
                            doener en grondlegger van een vorm van hulpverlening die gestart is binnen de
                            muren van een kleine eerstelijnspraktijk in de psychologie. Aan huis. Nu, bijna
                            20 jaren later, is er door allerlei bewegingen een multidisciplinair
                            gezondheidscentrum ontstaan waar middels een combinatie van fysiek en
                            psychologisch onderzoek, fysieke en psychologische training en therapie,
                            allerlei hulpvragen getackeld kunnen worden.</p>

                            <p><strong class="subtitel">Hoe het begon...</strong></p>

                            <p>Op de middelbare school werd ik –bleek achteraf- getriggerd door een pracht
                            van een film "An Early Frost" waar ik een groep mensen in therapie zag bewegen
                            door een simpel "iets". Ik werd in beweging gezet,&nbsp;maar onbewust. Op dat
                            moment was ik vrij "exact" van aard: ik deed wiskunde, natuurkunde, scheikunde.
                            Vakken waarbij logica de boventoon voeren. Ik zou astronomie gaan studeren in
                            Leiden en heb dat gedaan, voor 1 week. Ik heb toen bewust de keuze gemaakt op
                            zoek te gaan naar dat “iets”: Wat beweegt mensen? Hoe zit een mens in elkaar?
                            Wat maakt dat mensen soms vast komen te zitten? Ik ontdekte dat psychologie
                            (het werken met “het hoofd” alleen) wel een basis kon bieden voor bepaalde
                            vraagstukken, maar dat het lichaam ook veel doet tijdens zo’n proces. Ik
                            verwonderde mij over de vele aanwezige koppelingen tussen lijf en
                            leden,&nbsp;die ik ging onderzoeken, toepaste&nbsp;en wat ik meer en meer als
                            “eigen” ging beschouwen. Daarnaast&nbsp;zag ik&nbsp;de invloed van systemen op
                            mensen waarin ze verkeren, o.a. op het thuisfront, op school, in het werk, in
                            de maatschappij of cultuur. Maar draaide die gedachte al vrij snel om: wat kun
                            jij doen om invloed te hebben? Wat kun jij doen om de kwaliteit van je eigen
                            functioneren te verbeteren?</p>

                            <p>Juist door het werken als psycholoog, met name binnen diverse
                            Reïntegratie-bureaus, stuitte ik op dat wat ik als een bijzonder speelvlak
                            ervoer: een combinatie van alles wat zich rondom de mens beweegt en hoe dit
                            systeem te optimaliseren. Middels filosoferen, anders-denken , veel proberen en
                            te doen en creatief daarmee bezig te zijn werd een idee ontwikkeld.</p>

                            <p>Samen staan we als team bekend om onze eigenwijsheid maar waarin we dat
                            "iets" inzetten waar ik, jaren geleden, al een start mee heb gemaakt en wat nog
                            steeds in beweging is.</p>

