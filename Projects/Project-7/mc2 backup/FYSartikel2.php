                                <h2>'Bewegen verbetert stressreactie hersenen'</h2><br/>
                                <span style="COLOR: #808080"><strong>Beweging reorganiseert de
                                hersenen, waardoor het beter kan reageren op stress. Spanning heeft
                                daardoor minder invloed op de normale hersenfunctie.</strong></span>
                                <p><span style="COLOR: #808080">Dat schrijven onderzoekers van de
                                Princeton University in het Journal of Neuroscience. Wanneer muizen
                                mochten bewegen, vertoonden hun hersenen bij de blootstelling aan een
                                stressor (koud water) een piek in het aantal neuronen dat opwinding
                                uitschakelt in de ventrale hippocampus. Dit hersengebied is betrokken
                                bij het reguleren van spanning.</span></p>

                                <h2>Aanpassen</h2>
                                <p><span style="COLOR: #808080">"Het onderzoek laat zien dat de
                                hersenen zich gemakkelijk aanpassen aan de leefstijl en de omgeving",
                                vertelt onderzoeker Elizabeth Gould. "Als we begrijpen hoe de hersenen
                                spanningen verwerken, kunnen we mensen met angststoornissen ook beter
                                helpen."</span><br /><br />
                                <span style="COLOR: #808080">Voor het onderzoek kreeg een groep muizen
                                onbeperkt toegang tot een loopwiel, terwijl een tweede groep muizen
                                daar niet over beschikte. De muizen met een actieve leefstijl hadden na
                                blootstelling aan koud water duidelijke controle over hun
                                stressreactie. Bij de tweede groep ontstond direct
                                opwinding.</span></p>

                                <h2>Oorzaak</h2>
                                <p><span style="COLOR: #808080">Een bepaalde neurotransmitter
                                (signaalstof), GABA, lijkt een belangrijke rol te spelen in deze
                                reactie. GABA vermindert de neuronale opwinding. Zodra de receptoren
                                voor deze neurotransmitter werden geblokkeerd, verdween het
                                spanningsverminderende effect van beweging.</span></p>
