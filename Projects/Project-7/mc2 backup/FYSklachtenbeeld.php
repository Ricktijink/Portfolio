                            <h2>Overbelastingsklacht</h2>
                            <p>Wanneer je een bepaalde structuur (spier, gewricht, bot, pees, enz.) in je
                            lichaam TE zwaar belast over een lange periode in tijd kun je een (pijn)klacht
                            ontwikkelen. Het is dan zaak dat we de lokale overbelasting laten herstellen.
                            In dat geval zullen we therapie toepassen om het herstel te
                            bevorderen/stimuleren. Je kunt hierbij denken aan verschillende manuele vormen
                            zoals onder andere massage en mobilisaties. Wanneer de (pijn)klacht afgenomen
                            is zullen we de overstap maken naar een training waarbij we de structuur en en
                            omliggende structuren belastbaarder maken. Dit zodat in de toekomst dezelfde
                            klacht niet zal terugkeren.<br /><br />
                            Daarnaast zullen we energie en besteden aan het geven van uitleg en advies met
                            betrekking tot hoe je het beste kunt bewegen om te voorkomen dat deze klacht in
                            de toekomst terugkomt.</p>
                        </div><br/><br/>
                    </div>
                    <div id="Inhoud3">
                        <div id="fysklacht3" class="editable">
                            <h2>Chronische klachten</h2>
                            <p>Wanneer een lichamelijke klacht langer dan 3 maanden aanwezig is spreken we
                            van een chronische klacht. Bij het behandelen van deze klacht is het extra
                            belangrijk dat we naast het optimaliseren van het herstelproces ook gaan kijken
                            naar hoe je met de klacht omgaat; wat zijn je beweegpatronen, hoe zou het
                            kunnen komen dat de klacht niet weggaat en welke factoren hebben een
                            positieve/negatieve invloed op de klacht.<br /><br />
                            Afhankelijk van de aard, de duur en de intensiteit van je (chronische)klacht
                            wordt bepaald welke behandelmogelijkheden we gaan toepassen. Je kunt denken aan
                            trainingsvormen, technieken op de behandelbank en informerende
                            gespreksvoering.&nbsp;MC<sup>2</sup> is juist gespecialiseerd in het behandelen
                            en begeleiden van mensen met chronische klachten.</p>
                        </div><br/><br/>		
