                            <h2>Post-operatief</h2>
                            <p>Wanneer je geopereerd bent aan het houding- en bewegingsapparaat kan het erg
                            belangrijk zijn om spoedig te starten met het mobiliseren en trainen van
                            bepaalde structuren. Wij zullen in dat geval samen met jou deze training
                            oppakken. We starten na een intake met het revalidatie traject. Binnen dit
                            traject zullen aanvankelijk passieve of geleid actieve mobiliserende oefeningen
                            ingezet worden en gedurende het traject, afhankelijk van de soort operatie en
                            de fase van herstel, zal dit overgaan in actieve oefentherapie waarin we de
                            regio op kracht en stabiliteit zullen trainen.<br /><br />
                            Daarnaast is het ook altijd belangrijk om op de hoogte te zijn van alle
                            belangrijke houding- en bewegingsadviezen. Door deze correct toe te passen kun
                            je voorkomen dat de geopereerde regio overbelast raakt in de eerste periode na
                            de operatie en in de toekomst.<br /><br /></p>
