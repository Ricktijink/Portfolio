                            <h2>Sportletsel</h2>
                            <p>Waar gesport wordt ontstaan blessures. Dit zo genoemde sportletsel kan acuut
                            ontstaan (bijvoorbeeld het verdraaien van een knie). Een sportletsel kan zich
                            echter ook langzaam ontwikkelen, zoals andere <span style=
                            "COLOR: #fc3">overbelastingsklachten</span>. In beide gevallen is het
                            belangrijk te achterhalen wat de oorzaak is van het letsel. Is het een puur
                            mechanisch probleem ( bijvoorbeeld een TE grote kracht die een spier deed
                            scheuren) of is er ergens in het lichaam een probleem aanwezig welke de klacht
                            tot gevolg heeft (denk aan een knieklacht door een platvoet). Wanneer we het
                            sportletsel behandelen kijken we dus altijd naar het totale lichaam.<br /><br />
                            Afhankelijk van de aard, de duur en de intensiteit van je (sport)klacht wordt
                            bepaald welke behandelmogelijkheden we gaan toepassen. Je kunt denken aan
                            trainingsvormen, technieken op de behandelbank en informerende
                            gespreksvoering.</p>
