                            <h2>Huisbehandeling</h2>
                            <p>Er zijn over het algemeen twee redenen waarom er gekozen kan worden voor een
                            huisbehandeling: Wanneer het door je klachtbeeld niet mogelijk is om naar de
                            praktijk te komen voor een behandeling, komen wij bij je&nbsp;thuis. Wanneer
                            nodig, nemen we zelfs we wat oefenmaterialen mee om de behandeling zo optimaal
                            mogelijk te maken, met maximaal resultaat. De tweede reden is wanneer je een
                            zogenaamd ADL-specifieke vraag hebt. Bijvoorbeeld als opstaan uit een stoel bij
                            ons op de praktijk goed gaat maar bij je thuis uit de bank opstaan niet. We
                            kunnen dan op huisbezoek komen om te onderzoeken waarom het niet lukt en
                            aansluitend te behandelen, om zo te zorgen dat het daarna wel lukt.</p>
