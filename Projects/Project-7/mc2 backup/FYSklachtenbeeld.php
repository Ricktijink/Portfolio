                            <h2>Medische fitness</h2>
                            <p>Na afsluiting van een succesvolle behandeling is het enorm belangrijk het
                            huidige fysieke niveau te behouden. Vaak doen mensen dit door de oude
                            sportactiviteit te hervatten of door een nieuwe te starten. Voor deze laatste
                            mogelijkheid is de medische fitness die wij verzorgen een erg goede optie. 1 of
                            2 keer per week train je in groepsverband onder begeleiding van een
                            fysiotherapeut die volledig op de hoogte is van je lichamelijke belastbaarheid.
                            Vooral mensen die een stok achter de deur nodig hebben om het sporten vol te
                            kunnen houden zijn in de medische fitness op de juiste plek. De
                            ‘personal-touch’ en groepssfeer zorgen voor de extra motivatie die nodig is om
                            structureel te blijven sporten/bewegen.</p>
                            <p>Voor meer informatie over de Medische fitness kijk je bij <a href=
