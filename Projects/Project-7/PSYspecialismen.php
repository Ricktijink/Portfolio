                            <h2>EMDR</h2>
                            <br/>
                            <strong>MC² biedt EMDR behandeling</strong><br/><br/>
							<strong>Wat is EMDR?</strong><br/>
							<p>Eye Movement Desensitization and Reprocessing, afgekort tot EMDR, is een therapie voor mensen die last blijven houden van de gevolgen van traumatische of nare ervaringen. Dit kan zijn een schokkende ervaring, zoals een verkeersongeval of een geweldsmisdrijf. Maar ook voor andere ervaringen die veel invloed hebben gehad op de ontwikkeling van iemands leven zoals pesterijen of moeilijke gebeurtenissen in de jeugd, die in het hier-en-nu nog steeds invloed hebben kan de methode gebruikt worden.</p>
							<strong>Wat is het doel?</strong><br/>
							<p>De belangrijkste insteek van de EMDR is de cliënt te helpen de herinneringen aan deze gebeurtenissen te verwerken, met de bedoeling daarmee de klachten te verminderen of te laten verdwijnen. Met verwerken wordt niet bedoeld dat de herinnering verdwijnt, het betekent dat de lading van de herinnering verminderd of de gebeurtenis een andere betekenis krijgt.</p>
							<strong>Wanneer is EMDR bij MC² niet geschikt?</strong><br/>
							<p>MC² biedt EMDR binnen de generalistische basis GGZ. Dit betekent dat het veelal om korte trajecten gaat. In een intakegesprek kunnen we beoordelen of een kort EMDR traject voldoende is, of dat een verwijzing naar de specialistische GGZ (bijvoorbeeld bij complexe, langdurige of veelvuldige trauma’s) meer passend is.</p>
							<strong>Ervaringen van therapeut en cliënt</strong><br/>
							<p>Lisette (psycholoog MC²): ‘Toen ik begon met het geven van EMDR was ik sceptisch: kan dit echt zorgen voor een verlichting van klachten?  Wat een raar gedoe dat bewegen met mijn vingers, haalt dat echt iets uit? Zijn mensen wel in staat om zo levendig terug te denken en zo’n nare ervaring? Omdat mijn opleider en collega’s die reeds EMDR gaven heel enthousiast waren, besloot ik dit met de opleiding in de praktijk te brengen. Mijn praktijkervaring? EMDR werkt. Bij de een sneller dan bij de ander, maar ik durf te zeggen dat EMDR tot nu toe bijna altijd een (positief) effect teweeg gebracht heeft. Mijn ervaring is dat EMDR ook goed kort of zelfs eenmalig ingezet kan worden, om een vastgelopen behandeling weer in beweging te zetten. Kortom, ik ben om en zie EMDR als een mooie aanvulling op het therapieaanbod van MC²’.  </p>
							<p>Marlies (cliënte MC²): ‘Ik was in eerste instantie sceptisch t.o.v. EMDR. Omdat ik zo vast zat in het verdriet bij het overlijden van mijn man, heb ik hulp gevraagd bij MC². Het ziekteproces van mijn man was erg intensief en ingrijpend met nare herinneringen / beelden, die ik moeilijk kon verwerken. Door de behandeling, waarvan ik eerst “wilde zien en dan geloven”  hebben de beelden, die voor mij emotioneel heel zwaar waren, een andere lading gekregen. Vraag me niet hoe het komt, maar het hielp en helpt mij nog steeds. Het is goed om te ervaren dat EMDR een positieve bijdrage heeft gegeven aan mijn verwerkingsproces’.</p>
