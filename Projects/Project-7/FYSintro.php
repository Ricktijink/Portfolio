                                <h2>Introductie Fysiotherapie</h2>
                                <p>Een fysiotherapeut helpt bij het verhelpen én voorkomen van lichamelijke klachten. Het kan bijvoorbeeld gaan om hoofdpijn, nekklachten of herstel na een operatie (zie voor andere voorbeelden ook het kopje "<a title="LinkFysKlachtbeeld" href="FYSklachtenbeeld.php" target="_self">Klachtbeelden</a>"). Tijdens de intake wordt door de fysiotherapeut een specifiek beeld gevormd omtrent je lichamelijke (pijn)klacht. Daarnaast kijken we niet alleen naar de botten, spieren, zenuwen, etc. maar indien geïndiceerd ook naar het psychosociale aspect van de klacht. We kijken ook naar hoe je in het dagelijks leven omgaat met lichamelijke klachten. En andersom, naar de mogelijkheid dat de fysieke klachten voortkomen uit mentale (over)belasting. Iets dat bijvoorbeeld het geval kan zijn bij een burn out.</p>
                                <p>Of je nu een enkelvoudige fysieke klacht hebt of een chronische klacht, bij de fysiotherapeuten van MC² ben je aan het juiste adres.</p>
                                <p>Voor het maken van een afspraak bij MC² heb je geen verwijzing van de huisarts nodig. De fysiotherapeut informeert de huisarts wel, met jouw goedkeuren.</p>
                                <p>Wij hebben in onze 'bewegingszaal' de beschikking over diverse oefenmaterialen en alle noodzakelijke soorten moderne trainingsapparatuur, zoals een Technogym 'Kinesis' driedimensionaal trainingsapparaat, twee loopbanden, hometrainer en ligfiets, twee crosstrainers en een multifunctionele krachttrainer. Hierdoor zijn wij in staat om iedere fysieke klacht zeer gericht en op de best mogelijke wijze te behandelen.</p>
