                            <h2>Ervaring van een cliënt</h2><br />
                            <strong class="subtitel">Op avontuur met MC²</strong><br />

                            <p>Eigenlijk weet je het al maar je durft er niet aan toe te geven. De signalen
                            zijn duidelijk. Veel hoofdpijn, moeilijk in slaap komen, ’s morgens
                            oververmoeid opstaan, moeilijk werk uitstellen, hartritmestoornissen en
                            concentratieproblemen. Je weet dat je moet kiezen voor een time-out, maar je
                            bent bang om te vallen. Nu weet je tenminste nog wat je hebt, ook al ben je
                            daar niet gelukkig mee, en dat alternatief ken je niet. Hoe reageer je op het
                            thuis zijn? Wat vinden anderen ervan? Je kunt ook niet gemist worden want alles
                            draait toch om jou?<br />
                            Toen ik Marco (red: psycholoog bij MC²) voor de eerste keer ontmoette verdween
                            mijn angst om mijn zekerheden los te laten. Vanaf het eerste moment merkte ik
                            een oprechte belangstelling voor mijn persoon en alle belemmeringen die ik in
                            me had. In deze relatie durfde ik me wel bloot te geven, mijn angsten en
                            frustraties op tafel te leggen en mezelf over te leveren aan dit avontuur met
                            Marco als gids. Zowaar een hele stap voor iemand die al zijn leven lang meent
                            alle touwtjes in handen te moeten hebben.</p>

                            <p>Het avontuur is begonnen. Ik val niet, maar ik glijd naar het niveau van
                            niet meer functioneren. Ik geef me over aan de wereld van de slaap. Elk moment
                            van de dag blijkt ineens geschikt te zijn om even de ogen te sluiten en je over
                            te geven aan de vermoeidheid die tot in je kleine teen zit opgeslagen. Je
                            fysieke klachten zijn nog volop aanwezig. Zowel de huisarts als de cardioloog
                            doen hun best om vast te stellen waarom mijn lichaam niet functioneert zoals
                            het hoort, maar ze vinden geen oorzaak. Alle verschijnselen worden
                            toegeschreven aan de stress waarin je verkeert.<br />
                            In deze fase ontmoet ik Cindy (red: voormalig oefentherapeute Cesar bij MC²) en
                            voor het eerst en leer ik de beginselen van het luisteren naar je lichaam. Elk
                            spiertje in mijn lijf wordt aangespannen en ontspannen en ik voel de
                            verschillen. Ik leer mijn lijf zich weer te ontspannen en ik kan dat zelf
                            regelen boven in die hersenkamer. Algehele controle heb ik nog niet want
                            spontaan snurk ik af en toe nog even tijdens de oefening. Maar ook dat is
                            veelzeggend. Kennelijk ervaar ik geen schaamte in de aanwezigheid van anderen
                            in deze ruimte en met deze begeleiding.<br />
                            Al luisterend, pratend, lerend en oefenend ervaar ik minder spanning in mijn
                            lijf. De fysieke klachten verdwijnen gaandeweg. Wonderlijk wat er kan
                            veranderen als je leert luisteren naar je lichaam.</p>

                            <p>Het werken aan mijn herstel lijkt een ingewikkeld avontuur. Maar al het
                            andere lijkt ook ingewikkeld. Ik denk ingewikkeld want er is veel chaos in mijn
                            hoofd. Ik denk sowieso veel te veel en kennelijk heb ik ook veel te lang teveel
                            gedacht. En hoe is het met het voelen? Ik voel alleen op de momenten dat ik
                            iets ervaar en in dit avontuur gaat het voelen er soms heftig aan toe. Ik heb
                            geen gebrek aan emoties, integendeel, ik loop af en toe over van emoties. Hoe
                            lang is het geleden dat ik mijn denken heb afgestemd op mijn voelen, dat ik bij
                            een beslissing mezelf heb afgevraagd hoe dat voelt? Marco zegt dat ik iets voor
                            mezelf moet gaan doen. Iets kleins, dat goed voelt en dat helemaal voor mezelf
                            is.</p>

                            <p>En zo brengt Marco een complexe opdracht van “afstemmen van denken op
                            gevoel” terug tot een eenvoudig “doe iets kleins voor jezelf wat je leuk vind”.
                            Ondanks de eenvoud blijkt het een moeilijke opgave. Ik heb verleerd om iets
                            voor mezelf te doen. Het komt niet meer voor in mijn geprogrammeerd geheugen.
                            Herprogrammeren dus, ook dat hoort bij dit avontuur.</p>

                            <p>Soms merk je dat je niet de enige avonturier bent. Lotgenoten zitten met je
                            aan tafel en al gauw ervaar je dat zij om hele andere redenen mee mogen reizen.
                            Dat vind ik wel interessant en voor ik het weet mag ik ook even gids zijn, hun
                            gids wel te verstaan want mezelf gidsen is nog niet aan de orde.<br />
                            Ik heb een mening en zowaar als ik die geef merk ik dat die er ook toe doet. Ik
                            heb mijn buurman een stukje de weg gewezen waardoor zijn reis weer even
                            duidelijk wordt. Dat geeft me energie, ik heb weer wat vertrouwen in mezelf
                            gewonnen. Misschien lukt het me een keer om weer mijn eigen gids te zijn. Die
                            gedachte voelt goed.</p>

                            <p>Het is geen kant en klare reis, dat avontuur met Marco en Cindy. Excursies
                            zijn er niet. Vaak wordt me de vraag gesteld: “Wat wil je dat wij de komende
                            tijd voor jou gaan betekenen?” Dan denk ik: “Hallo….. wie is hier nou de
                            reisleider?” En toch reis ik verder. Ik ga dan bij mezelf na waar ik ben, waar
                            ik naar toe wil en hoe ik daar graag wil komen. En als ik dat dan even moeilijk
                            vind en naar die ogen van Cindy kijk, lees ik haar onuitgesproken zin: “Je komt
                            wel met iets want je weet zelf maar al te goed dat je verder wilt in deze
                            reis.” Zo is het ook. Het is immers mijn reisje.</p>

                            <p>Mijn energie neemt toe, ik voel me goed, het weer is mooi, het eindpunt van
                            de reis lijkt in zicht. Maar hoe ziet dat eindpunt eruit? Wat ga ik dan doen?
                            Kan ik dat en mag ik dat? Wat vinden anderen dan dat ik moet doen en vind ik
                            dat dan leuk? Teveel vragen en antwoorden krijg ik nog niet. Dan vul ik die
                            zelf wel in, mijn energie vraagt namelijk om die antwoorden. Het blijken niet
                            de antwoorden te zijn die goed voelen. En juist dat voelen wordt nu toch wel
                            heel heftig. Mijn lijf reageert meteen, het protesteert met alle signalen die
                            het in zich heeft. De energie lekt van alle kanten en ik zou er bijna mijn
                            leven voor over hebben om even mijn hoofd van mijn lichaam te kunnen
                            nemen.<br />
                            Ook dan blijven de reisleiders rustig. Ze zijn er, helemaal voor jou. Je voelt
                            weer dat je niet alleen bent in dit avontuur, ze doen mee. Sneller dan voorheen
                            bereik je weer een hoger niveau. Accepteer maar dat het avontuur nog niet af is
                            maar weet wel, langzaam maar zeker word je weer je eigen gids en jij zelf zegt
                            wanneer het zover is.</p>

                            <p>Mijn avontuur is nog niet af en misschien blijft mijn hele leven wel een
                            avontuur. Ik ben dan wel mijn eigen gids. Ik weet waar de valkuilen liggen,
                            waar het té spannend is en waar het té saai is. Ik wil een reis op maat en
                            onderweg stuur ik Cindy en Marco af en toe een leuke ansichtkaart.</p>
