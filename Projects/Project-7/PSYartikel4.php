                            <h2>Wat is stress? Oorzaken en symptomen</h2><br />
                            <strong class="subtitel">Stress. Er wordt veel over gepraat en iedereen heeft
                            er wel eens last van. De een kan er beter tegen dan de ander. Maar wat is het
                            nu eigenlijk stress?</strong><br />

                            <p>Stress betekent niets anders dan spanning of druk. Van oorsprong is het een
                            term uit de bouw: stress is de kracht die wordt uitgeoefend op een voorwerp. Of
                            stress schadelijk is hangt af van hoeveel belasting iets (of iemand) aan kan.
                            Zelfs metaal krijgt haarscheurtjes als het te eenzijdig of constant wordt
                            belast. Dat geldt dus ook voor mensen.</p>

                            <p>Als we het over stress hebben, dan gaat het meestal over ongezonde druk: een
                            teveel aan spanning. Maar stress is niet altijd ongezond. Stress is een hele
                            natuurlijke, en ook gezonde reactie van het lichaam. Op het werk helpt gezonde
                            stress je om goed te presteren. Het maakt je alert, geconcentreerd en
                            efficiënt. De spanning maakt het lichaam ook klaar voor actie en dat is nodig
                            om te kunnen reageren op bedreigende situaties.</p><strong class=
                            "subtitel">Evenwicht</strong><br />

                            <p>De problemen ontstaan pas wanneer de draaglast groter wordt dan de
                            draagkracht. Op dat moment zijn de eisen die aan jou worden gesteld of die je
                            aan jezelf stelt groter dan wat je aankunt. Het evenwicht tussen de factoren
                            die spanning veroorzaken, stressoren, en jouw mogelijkheden om stress te
                            voorkomen of er mee om te gaan slaat door naar de verkeerde kant.<br />
                            Een verstoring van dat evenwicht kan veroorzaakt worden door een te grote
                            draaglast of een te kleine draagkracht. Bij een te grote draaglast is er sprake
                            van eisen en oorzaken buiten jezelf als persoon. Deze belastende omstandigheden
                            kunnen heel verschillend zijn: variërend van alledaagse stress-situaties tot
                            ingrijpende levensgebeurtenissen.</p><strong class=
                            "subtitel">Eigenschappen</strong><br />

                            <p>Sommige mensen zijn gevoeliger voor stress dan anderen: de draagkracht. De
                            volgende eigenschappen maken kwetsbaar:<br />
                            <br />
                            • Perfectionisme<br />
                            • Zogenaamde type A-factoren: ambitie, prestatiegerichtheid, competitie,
                            gehaastheid, niet 'niets' kunnen doen, vaak twee dingen tegelijk doen<br />
                            • Veel van zichzelf eisen en 'moeten'<br />
                            • Groot verantwoordelijkheidsgevoel<br />
                            • Grote betrokkenheid bij gezin of werk<br />
                            • Streven naar waardering van anderen<br />
                            • Moeilijk 'nee' kunnen zeggen, grenzen kunnen aangeven of voor zichzelf
                            opkomen<br />
                            • Moeilijk steun kunnen vragen<br />
                            • Gevoelens slecht kunnen uiten<br />
                            • Gevoel weinig invloed te kunnen uitoefenen op de omgeving en het eigen
                            leven<br />
                            • Pessimisme en een weinig positieve kijk op eigen prestaties</p>

                            <p>Deze eigenschappen worden deels bepaald door karakter en temperament, maar
                            ervaring en vaardigheden spelen ook een grote rol. Je kunt zelf veel doen om je
                            draagkracht te verbeteren. Vaardigheden als assertiviteit en conflicthantering
                            kun je verbeteren en ook kun je leren meer realistische eisen te stellen.<br />
                            <br />
                            Ook ongezonde leef- en werkgewoonten kunnen de draagkracht verminderen.
                            Verkeerde eetgewoonten, te veel koffie of alcohol, te weinig lichaamsbeweging
                            en een tekort aan slaap en ontspanning bijvoorbeeld. Maar ook een slechte
                            organisatie van het werk, te veel werken en te weinig pauzes nemen dragen bij.
                            Je weerstand vermindert en daardoor ben je vatbaarder voor de gevolgen van
                            stress.</p><strong class="subtitel">Symptomen</strong><br />

                            <p>Ongezonde stress kan zich op vier verschillende manieren uiten:<br />
                            • Lichamelijke signalen<br />
                            • Psychische signalen<br />
                            • Gedragssignalen<br />
                            • Gedachten- of denksignalen</p><strong class=
                            "subtitel">Lichamelijk</strong><br />

                            <p>Lichamelijke signalen hangen samen met de verhoogde lichamelijke activiteit
                            bij stress. Je kunt last krijgen van hoofdpijn, rugpijn of nekpijn, maar ook
                            van hartkloppingen of spijsverteringstoornissen. Het afweersysteem kan door
                            stress worden aangetast. Hierdoor word je ook vatbaarder voor verkoudheid en
                            griep. Vermoeidheid, rusteloosheid en slaapproblemen zijn ook veel voorkomende
                            stresssymptomen.</p><strong class="subtitel">Psychisch</strong><br />

                            <p>Gestreste mensen zijn vaak prikkelbaar of cynisch. Daarnaast kan stress ook
                            gepaard gaan met gevoelens van machteloosheid, ongeluk of verveling. Ongezonde
                            stress kan de stemming, de gemoedstoestand waarin iemand verkeert, sterk
                            beïnvloeden. Mensen kunnen zich ook eenzaam voelen.</p><strong class=
                            "subtitel">Gedrag</strong><br />

                            <p>Bazig, snauwen, kritisch, maar ook overmatig gebruik van alcohol, drugs,
                            sigaretten of eten. Ongezonde stress kan ook veranderingen in het gedrag van
                            mensen veroorzaken. Deze veranderingen komen onder andere voort uit de
                            lichamelijke en psychische symptomen van stress. Prikkelbaarheid kan leiden tot
                            conflicten en lusteloosheid of somberheid kan juist terugtrekken uit sociale
                            contacten tot gevolg hebben.</p><strong class="subtitel">Denken</strong><br />

                            <p>Tot slot hebben mensen met stress vaak moeite met helder nadenken. Een
                            gebrek aan creativiteit, geheugenproblemen, concentratieproblemen, het komt
                            allemaal voor. Ongezonde stress leidt vaak tot een negatief gedachtenpatroon:
                            mensen kunnen hun gevoel voor humor kwijt raken.</p><br />

