                 <h2 class="postbig-title">When you put it that way</h2>
                  <div class="postbig-info">
                     <!-- Author of the post -->
                     <i class="far fa-user"></i>Faraz Saberi |
                     <!-- Date of the post -->
                     <i class="far fa-clock"></i>05-02-2019 |
                     <!-- Category of the post -->
