                     Image: Some random lady from the internet!
                  </div>
                  <div class="postbig-text">
                     <p class="text-intro">
                        At first glance it is just a gazebo, another tiny wooden house that dots the North West university campus landscape. Located in the park 
                        across from my dorm, it looks almost as old as the carved stone slab that rests in its center. If you’ve ever been there, you’ll know it’s 
                        an important place for the locals not just by the great big slab of carved stone that sits in its middle or by the many people who frequent
                        the place regardless of weather or season. You’ll know it in the worn down stone you stand on, or in the butt-shape of the weather-beaten 
                        wooden benches that have supported hundreds of thousands of butts in the last century alone. This place is a tiny temple of peace in the 
                        midst of the maelstrom of the busy Xi’anese life and it’s the place where the young come to hang between classes, adults come together to 
                        drink tea and the elderly come to ruminate on how the years have gone or to pay respects to those who they used to warm the old benches with them. 
                     </p>
                     <br>
                     <p>
                        More recently though – depending on how old you are and what you think recently means – it has also become a place where North-west university’s 
                        foreign exchange students come together. Students from all over the world go there to hang out, drink tea and practice their Chinese and in the 
                        space of two years, I’ve seen this pagoda be the start of friendships between German,  Korean, Uzbeki, Belgian, Chinese, American, Italian, Dutch,
                        Russian, Kazakh, Thai and other students from all over the world. I’ve seen people from completely different cultures strike up conversations there.
                        I’ve seen the old people teach the young newly arrived exchange students as if they’re old friends. I’ve made friends there, played cards there, 
                        had birthdays there and learned to toast to people’s health in more than 10 different languages. In short, that gazebo holds more memories for me 
                        every single day and whenever I pass it by and see who’s there now.

                     </p>
                     <br>
                     <p>
                        Right now in fact, I’m passing the place as I and a Korean friend have just come back from lunch and we are simply crossing the garden the place sits in on our way to our respective rooms when my eyes are once more to drawn to the old thing. Or rather, the people sitting on its benches.
                        Among the current inhabitant is Leila, A Kazakhstani girl who’s goes to the same language classes I do. She’s currently talking to two young bearded faces I’ve never seen before and when she sees me, she waves me over. I go over and introduce myself to the two gentlemen who turn out to be newly-arrived students from Turkey.
                     </p>
                     <br>
                     <p>
                        Their Chinese and English are pretty good and we talk for a while. I introduce them to my Korean friend and we talk as we sip on our tea. It isn’t long before we find common ground and start joking around as men do and several shades of extraversion start seeping out. You know that thing men do. We hype each other up and get louder as we go along. Pretty soon, we’re hooting and laughing and my Korean friend tells me I’m being too Dutch, by which she means I’m being too loud. She’s not wrong. Dutch people do have a bit of a reputation of being loud, outgoing and confident to a fault. Apparently, I fit that stereotype perfectly, which is why I take it as a compliment and grin back at her. When I look back over to the two guys, I see they’ve struck up a conversation with an older lady on the other side of the pagoda. A conversation isn’t the right word though, since a conversation is a two-way stream. This convo seems particularly one-sided. The lady in question is sitting by herself, her posture ram-rod straight and her legs crossed in that statuesque way only the elderly ladies of the most stern sort manage to pull off. She’s staring out over the tiny park as if lost in thought, but I can tell she’s heard us and that she knows we’re trying to practice our Chinese. 
                     </p>
                     <br>
                     <p>
                        “Hey, young lady, what’s your name?”, the guy continues as he tries to draw her into a conversation. Having played this game before I know what the guy’s end-goal is. 
                        He’s trying to draw a smile from the older woman. He’s literally trying to turn that frown upside down. To no avail, however, since the lady  doesn’t even spare him a glance let alone words.
                        The boy isn’t discouraged however and his smile grows wider as his friend is the next to try. His friend’s Chinese isn’t all that good though and before long his attempt devolves into our laughter.
                        “I’m leaving”, my Korean friend says and I’m pulled out of the game and back to my Korean friend who’s looking distinctly uncomfortable. 
                        I turn around to the men and my friend Leila, shake their hands again and walk back with my Korean friend. As we walk back, I notice she’s silent.
                        <br> 
                        “Everything alright?” I ask. She takes a moment before she answers with a question.
                        <br>
                        “Is that normal in your country?”
                        <br> 
                        “Normal? what do you mean?”
                        <br>
                        “Bothering older people like that…”
                        <br>
                        I look at her in consternation.
                        <br>
                        "Bothering her? The guy was simply asking her a question.”
                        My friend frowns and even looks a little angry but her eyes are curious and honest as she continues.
                        <br>
                        “They called her ‘young lady’! Even though she was clearly older than them. Is that normal in your country?”
                        Then I realize what’s going on. 
                        <br>
                        “No, no… I don’t know how they do it in Turkey but what you just saw is normal to me. It’s what I do when I want to compliment older ladies…err… right! Like err… you know how when you meet your mother-in-law you might pretend that you think that she’s the older sister?”
                        <br>
                        “Wh-what?”
                        <br>
                        “You know.. to let her know she looks very young for her age,” I say. “That’s what the guy was trying to do. He was giving her a compliment.”
                        <br>
                        “Complimenting her by treating her long a young girl?”
                        <br>
                        “Well, yeah… I mean, that was the idea. She was sitting by herself while we were having fun right next to her. I think they wanted to make her smile too…”
                        <br>
                        “Smile, huh? It looked like they were trying to look cool in front of the girl to me…”
                        <br>
                        “Look cool?” It’s my turn to frown. They weren’t trying to look cool, were they? Well, I don’t know, maybe they were? But it wasn’t anything malicious…  It’s just boys being boys… like a game. The challenge was to make the sad lady smile.”
                        <br>
                        “Why?”
                        <br>
                        “Why? Isn’t trying to make someone smile a good thing?“
                        My friend tilts her head.
                        <br>
                        “They weren’t successful though…”
                        <br>
                        “That’s okay… embarrassing yourself in front of friends is no problem. It’s normal where I’m from…”
                        <br>
                        “So the fact that the lady ignored you didn’t bother you?”
                        <br>
                        “Why would it? Some  games are harder than others and it’s okay to be the loser once in a while. Besides, we were just trying to include her. To share some fun. Where I’m from, laughing about this stuff is how you connect.”
                        “You maybe, but that woman did not feel connected. She felt disrespected.”
                        <br>
                        “Just because we called her a young lady? What is she, the queen?” I’m actually miffed by this point, wondering how it’s okay that when students get approached in China it’s not rude but when we do it ourselves, it’s suddenly not okay... 
                        My friend lets out a grin. 
                        <br>
                        “You’re so easy to read.”
                        <br>
                        “Look, maybe that’s how you see it in your country but in mine, it’d be very rude…. I mean, imagine. You’ve lived for over 70 years and spent most of that time working for your country and family. Now after having made it to a respectable old age, you come to a gazebo, maybe for some peace and quiet, maybe to relive old times or even just to take a hard-earned nap. And then as you’re just minding your own business, some upstart foreign stranger disturbs your thoughts, completely ignoring social etiquette and treating you like a young girl to be flirted with.”
                        <br>
                        “…”
                        <br>
                        “….”
                        <br>
                        “Well shit…”
                        <br>
                        My friend laughs.
                        <br>
                        “You see what I mean?”, she asks.
                        <br>
                        “Yeah, when you put it that way…. But do you see what I mean?”
                        <br>
                        “What, calling her young as a compliment?”
                        <br>
                        I nod and look at her expectantly.
                        <br>
                        “No, it’s stupid…It’s insulting.”
                        <br>
                        “But he meant it as a compliment! As in she still looks young and beautiful!”
                        <br>
                        “But she’s not young or beautiful? Why lie?”
                        <br>
                        “I give up…”
                     </p>
